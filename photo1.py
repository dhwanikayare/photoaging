def normalize_pm25_aqi(aqi):

    if aqi is None:
        return 0.6

    if aqi <= 50:
        return 0.2

    elif aqi <= 100:
        return 0.4

    elif aqi <= 150:
        return 0.6

    elif aqi <= 200:
        return 0.8

    else:
        return 1.0

