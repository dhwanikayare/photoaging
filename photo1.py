# EXPOSURE RISK SCORE
# ------------------------------------------------------------

W_UV = 0.70
W_SMOKE = 0.15
W_POLL = 0.10
W_SUNSCREEN = 0.05

R_exp = (
    W_UV * U +
    W_SMOKE * S +
    W_POLL * P +
    W_SUNSCREEN * (1 - C_protection)
)

R_exp = clamp01(R_exp)

# ------------------------------------------------------------
# FINAL PHOTOAGING BURDEN INDEX
# ------------------------------------------------------------

ALPHA = 0.80

PBI = clamp01(
    ALPHA * P_vis +
    (1 - ALPHA) * R_exp
)

